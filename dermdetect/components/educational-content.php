        <h4>What is Skin Cancer?</h4>

        <p>
            Skin cancer can be defined as the uncontrolled growth of unwanted cells in the skins outermost layer. Early detection is crucial for successful treatment.
        </p>

        <br>

        <h4>Risk Factors:</h4>

        <p> * Prolonged exposure to the sun's harmful UV rays increases the risk of skin cancer.</p>

        <p> * People with fair skin are more susceptible to skin cancer.</p>

        <p> * An individual from a family with history of skin cancer is at higher risk.</p>

        <br>

        <h4>Prevention Tips:</h4>

        <p> * Sunscreen: Regularly apply broad-spectrum sunscreen with SPF 30 or higher. </p>

        <p> * Protective Clothing: Wear hats, sunglasses, and clothing to cover sun-exposed areas of the body.</p>

        <p> * Seek Shade: Limit exposure during peak sunlight hours. </p>
        
        <p> * Regular Checkups: Keep an eye on your skin and report any changes to a healthcare professional. </p>     
        
        <br>

        <h4> Why Early Detection Matters: </h4>
        
        <p> When detected early, skin cancer is treatable. Regular self-examinations and professional skin checks are essential for spotting any unusual changes in moles, spots, or skin texture.
        </p>

